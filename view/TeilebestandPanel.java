    public void setTableWidths(JTable table)
    {
        int[] arrWidths = {85, 120, 120, 120, 50, 100};
        TableColumn tc;
        table.setRowHeight(23);
        int i = 0;
        for (int width : arrWidths){
            tc = table.getColumnModel().getColumn(i++);
            tc.setPreferredWidth(width);
        }
    }
    
