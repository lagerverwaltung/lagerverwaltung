    /**
     * Validiert die Eingabe, erzeugt ein neues LagerbestandFilterModel und
     * refreshed die Lagerbestandstabelle
     * @param evt 
     */
    private void executeFilterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_executeFilterButtonActionPerformed
        
        HashMap<Integer, String> errors = FilterUiHelper.getInstance().validateLagerFilter(
                txfMengeVon.getText(),
                txfMengeBis.getText()
                );
        
        if(Misc.createErrorDialog(this, errors) == true){
            return;
        }
        
        LagerbestandFilterModel lfm = FilterUiHelper.createLFM(
                cbxLagertyp.getSelectedItem().toString(),
                cbxX.getSelectedItem().toString(),
                cbxY.getSelectedItem().toString(),
                cbxZ.getSelectedItem().toString(),
                txfMengeVon.getText(),
                txfMengeBis.getText(),
                txfGrund.getText(),
                txfBezeichnung.getText());

        try{
           refreshLagerbestandTableModel(lfm);
        } catch (SQLException ex) {
            Misc.printSQLException(this, ex);
        }
        this.dispose();
    }//GEN-LAST:event_executeFilterButtonActionPerformed

    /**
     * Läd die Lagerbestandstabelle mit den übergenenen Filtereinstellungen
     * nach
     */ 
    private void refreshLagerbestandTableModel(LagerbestandFilterModel lfm) throws SQLException {
        LagerbestandCollection lc = LagerbestandCollection.getInstance().applyFilter(lfm);
        LagerbestandTableModel lm = new LagerbestandTableModel();
        lm.setData(lc);
        lagerBestandTable.setModel(lm);
    }
    
