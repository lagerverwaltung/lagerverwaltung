    private void loadHlCbx() throws SQLException {
        cbxZ.removeAllItems();
        cbxX.removeAllItems();
        cbxY.removeAllItems();
        cbxZ.addItem("z");
        cbxX.addItem("x");
        cbxY.addItem("y");

        Lager hl = Lager.getLager(Lager.Lagerort.hochregal);
        int x = hl.getHoehe();
        int y = hl.getBreite();
        int z = hl.getTiefe();

        for (int i = 1; i <= x; i++) {
            cbxZ.addItem(i);
        }

        for (int i = 1; i <= y; i++) {
            cbxX.addItem(i);
        }

        for (int i = 1; i <= z; i++) {
            cbxY.addItem(i);
        }
    }
    
