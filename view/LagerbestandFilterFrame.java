    /**
     * Richtet den Filter rechtsbündig zum Hauptfenster aus
     * @param main
     */
    public void alignFilterMenu(Component main)
    {
        int x = main.getX();
        int width = main.getPreferredSize().width;
        int rightBorder = x+width;
        int newOrigin = rightBorder -this.getWidth()-9;
        this.setLocation(newOrigin, this.getY()+210);
    }
    
    /**
     * Füllt die QuellComboBoxen mit den größtmöglichen Werten
     * @author ssinger
     * @throws SQLException
     */
    private void loadCbx() throws SQLException {
        cbxZ.removeAllItems();
        cbxX.removeAllItems();
        cbxY.removeAllItems();
        cbxZ.addItem("z");
        cbxX.addItem("x");
        cbxY.addItem("y");

        int x = 0;
        int y = 0;
        int z = 0;
        
        Lager fl = Lager.getLager(Lager.Lagerort.freilager);
        Lager hl = Lager.getLager(Lager.Lagerort.hochregal);
        
        if(hl.getHoehe() > fl.getHoehe()){
            x = hl.getHoehe();
        }else{
            x = fl.getHoehe();
        }
        
        if(hl.getBreite() > fl.getBreite()){
            y = hl.getBreite();
        }else{
            y = fl.getBreite();
        }
        
        if(hl.getTiefe() > fl.getTiefe()){
            z = hl.getTiefe();
        }else{
            z = fl.getTiefe();
        }
        
        for (int i = 1; i <= x; i++) {
            cbxZ.addItem(i);
        }

        for (int i = 1; i <= y; i++) {
            cbxX.addItem(i);
        }

        for (int i = 1; i <= z; i++) {
            cbxY.addItem(i);
        }
    }
    
